import type { ChefOrder } from "@/store/order-store";

function daysAgo(days: number, hour: number, minute: number) {
  const date = new Date();

  date.setDate(date.getDate() - days);
  date.setHours(hour, minute, 0, 0);

  return date.toISOString();
}

